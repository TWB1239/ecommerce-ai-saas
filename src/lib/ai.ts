export async function callAI(
  type: keyof typeof SYSTEM_PROMPTS,
  userInput: Record<string, any>,
  searchContext?: string,
): Promise<string> {
  try {
    const baseSystemPrompt = SYSTEM_PROMPTS[type]

    let systemPrompt = baseSystemPrompt
    if (searchContext) {
      systemPrompt += `\n\n## 网络实时参考数据\n以下是从网络上获取的实时信息，请在分析中参考：\n${searchContext}\n\n注意：网络数据仅供参考，不要将搜索到的公司/产品名称作为你自己的，仅用于参考分析。`
    }

    let userMessage = ''
    for (const [key, value] of Object.entries(userInput)) {
      if (value !== undefined && value !== null && value !== '') {
        userMessage += `${key}: ${value}\n`
      }
    }

    const response = await getClient().chat.completions.create({
      model: 'deepseek-chat',
      messages: [
        { role: 'system', content: systemPrompt },
        { role: 'user', content: userMessage },
      ],
      temperature: 0.7,
      max_tokens: 4000,
    })

    return response.choices[0]?.message?.content || '分析失败，请重试。'
  } catch (error: any) {
    console.error('AI API Error:', error)
    throw new Error(`AI 分析失败: ${error.message}`)
  }
}

